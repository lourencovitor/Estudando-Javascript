// Sempre o simbulo da operação aritimetica vem antes e atribuição depois

const a = 7;

let b = 3;

b += a; // atribuição aditiva, é equivalente a b = b + a
console.log(b);

b -= 4 // atribuição subtrativa, é equivalente a b = b - 4
console.log(b);

b *= 2 // atribuição multiplicativa, é equivalente a b = b * 2
console.log(b);

b /= 2 //  atribuição dividida, é equivalente a b = b / 2
console.log(b);

b %= 2 // atribuição modular, é equivalente a b = b % 2
console.log(b);